'use strict';
// Fabric server creation/deletion via the Fabric meta API (meta.fabricmc.net).
// New servers default to Fabric using the latest stable loader + installer.
const fs = require('fs');
const fsp = fs.promises;
const path = require('path');
const crypto = require('crypto');
const { execFileSync } = require('child_process');
const cfg = require('./config');
const mc = require('./mc');

const META = 'https://meta.fabricmc.net/v2';
const MODRINTH = 'https://api.modrinth.com/v2';
const UA = { 'User-Agent': 'shaurycontroller-panel/1.0 (apsood@gmail.com)' };

// Direct URL to a ready-to-run Fabric server launcher jar.
function serverJarUrl(mcVersion, loader, installer) {
  return `${META}/versions/loader/${encodeURIComponent(mcVersion)}/${encodeURIComponent(loader)}/${encodeURIComponent(installer)}/server/jar`;
}
function serverJarName(mcVersion, loader, installer) {
  return `fabric-server-mc.${mcVersion}-loader.${loader}-launcher.${installer}.jar`;
}

async function getJSON(url) {
  const res = await fetch(url, { headers: UA });
  if (!res.ok) throw new Error('fetch ' + url + ' -> ' + res.status);
  return res.json();
}

async function gameVersions() {
  const list = await getJSON(META + '/versions/game');
  return list.map((v) => ({ version: v.version, stable: !!v.stable }));
}

async function latestStable(kind) { // 'loader' | 'installer'
  const list = await getJSON(META + '/versions/' + kind);
  const norm = list.map((x) => (kind === 'loader' ? x : x)); // both expose .version/.stable at top level
  const stable = norm.find((x) => x.stable) || norm[0];
  return stable.version;
}

async function metaInfo() {
  const [games, loader, installer] = await Promise.all([
    gameVersions(),
    latestStable('loader'),
    latestStable('installer'),
  ]);
  return { games, latestLoader: loader, latestInstaller: installer };
}

function slugify(name) {
  const s = String(name).trim().toLowerCase().replace(/[^a-z0-9_-]+/g, '-').replace(/^-+|-+$/g, '');
  return s;
}

// Pick a free TCP port not used by an existing panel server and not currently listening.
function findFreePort() {
  const used = new Set(cfg.load().servers.map((s) => s.port).filter(Boolean));
  let listening = new Set();
  try {
    const out = execFileSync('ss', ['-tlnH'], { encoding: 'utf8' });
    for (const line of out.split('\n')) {
      const m = line.match(/:(\d+)\s/);
      if (m) listening.add(parseInt(m[1], 10));
    }
  } catch {}
  for (let p = 25565; p <= 25700; p++) {
    if (!used.has(p) && !listening.has(p)) return p;
  }
  throw new Error('no free port found in 25565-25700');
}

async function createServer({ name, mcVersion, loaderVersion, installerVersion, memory }) {
  const id = slugify(name);
  if (!id) throw Object.assign(new Error('invalid server name'), { status: 400 });
  const conf = cfg.load();
  if (conf.servers.some((s) => s.id === id)) throw Object.assign(new Error('a server with that name already exists'), { status: 409 });
  const dir = cfg.serverDir(id);
  if (fs.existsSync(dir)) throw Object.assign(new Error('directory already exists: ' + dir), { status: 409 });
  if (!mcVersion) throw Object.assign(new Error('mcVersion is required'), { status: 400 });
  if (!/^[0-9A-Za-z._-]+$/.test(mcVersion)) throw Object.assign(new Error('invalid mcVersion'), { status: 400 });
  if (memory && !/^\d+[kKmMgG]?$/.test(String(memory))) throw Object.assign(new Error('invalid memory (e.g. 4G)'), { status: 400 });

  const loader = loaderVersion || await latestStable('loader');
  const installer = installerVersion || await latestStable('installer');

  await fsp.mkdir(dir, { recursive: true });
  try {
    // Download the ready-to-run Fabric server launcher jar.
    const res = await fetch(serverJarUrl(mcVersion, loader, installer), { headers: UA });
    if (!res.ok) throw new Error('failed to download Fabric server jar (' + res.status + ') for MC ' + mcVersion);
    const jarName = serverJarName(mcVersion, loader, installer);
    const buf = Buffer.from(await res.arrayBuffer());
    await fsp.writeFile(path.join(dir, jarName), buf);

    const port = findFreePort();
    await fsp.writeFile(path.join(dir, 'eula.txt'), 'eula=true\n');
    // Minimal server.properties; Minecraft fills in remaining defaults on first launch.
    await fsp.writeFile(path.join(dir, 'server.properties'),
      [
        '# Generated by shaurycontroller panel',
        'server-port=' + port,
        'sync-chunk-writes=false',
        'motd=' + id,
        '',
      ].join('\n'));

    const server = {
      id,
      name: id,
      mcVersion,
      loader,
      installer,
      jar: jarName,
      java: cfg.JAVA_DEFAULT,
      memory: memory || '4G',
      jvmArgs: cfg.DEFAULT_FLAGS,
      port,
      createdAt: new Date().toISOString(),
    };
    conf.servers.push(server);
    cfg.save(conf);
    return server;
  } catch (err) {
    // Roll back a half-created directory.
    try { await fsp.rm(dir, { recursive: true, force: true }); } catch {}
    throw err;
  }
}

async function deleteServer(id, { deleteFiles = true } = {}) {
  const conf = cfg.load();
  const idx = conf.servers.findIndex((s) => s.id === id);
  if (idx === -1) throw Object.assign(new Error('no such server'), { status: 404 });
  if (mc.isRunning(id)) await mc.stop(id);
  conf.servers.splice(idx, 1);
  cfg.save(conf);
  if (deleteFiles) {
    try { await fsp.rm(cfg.serverDir(id), { recursive: true, force: true }); } catch {}
  }
  // Clean run artifacts (keep backups).
  for (const f of [cfg.runLog(id), path.join(cfg.RUN_DIR, id + '.start.sh')]) {
    try { await fsp.rm(f, { force: true }); } catch {}
  }
  return true;
}

